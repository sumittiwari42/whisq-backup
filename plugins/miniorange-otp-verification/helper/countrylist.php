	public static function getCountryCodeList()
	{
		$refl = new ReflectionClass('CountryList');
		return $refl->getConstants();
	}

	public static function getDefaultCountryCode()
	{
		return !MoUtility::isBlank(get_option('mo_customer_validation_default_country_code'))
					? get_option('mo_customer_validation_default_country_code') : NULL;
	}

	public static function isCountrySelected($value)
	{
		return !MoUtility::isBlank(get_option('mo_customer_validation_default_country_code'))
					&& $value == get_option('mo_customer_validation_default_country_code');
	}

